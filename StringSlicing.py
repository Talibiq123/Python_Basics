# Start Index: End Index: Steps
message1 = 'HelloWorld'
print(message1)

# Index
print(message1[0])

# Positive Index
# Start Index
print(message1[0:])
# end index
print(message1[:10]) # exclude end index
# steps
print(message1[::1])
print(message1[::2])

# Start Index: End Index: Steps
print(message1[0:10:1])


# Negative Index    -ve start index: -ve end index: -step
message2 = "Thalapathi"

# index
print(message2[-5])

# -ve start index
print(message2[-5:])

# -ve end index
print(message2[-10:-1])

# -ve steps         Reverse of String
print(message2[::-1])
















