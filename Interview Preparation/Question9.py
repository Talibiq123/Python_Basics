# Write a function that prints all the prime numbers between 0 and n.

# def prime_number(number):

def all_prime_numbers(num):
    list1 = []
    for i in range(0, len(list1[num])):
        list1.append(i)
        return list1



n = int(input('Enter a Number: '))
result = all_prime_numbers(n)
print(result)